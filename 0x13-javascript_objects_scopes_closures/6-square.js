#!/usr/bin/node
const SquareParent = require('./5-square.js');
module.exports = class Square extends SquareParent {
  charPrint (c) {
    if (c === undefined) {
      super.print();
    } else {
      for (let i = 0; i < this.height; i++) {
        for (let j = 0; j < this.width; j++) {
          process.stdout.write(c);
        }
        console.log();
      }
    }
  }
};
